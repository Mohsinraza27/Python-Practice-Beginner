# and, or and not

x = 30 
y = 45
print(x == y and x != y)
print(x < y and x > y)
print(x <= y and x >= y)
print(x < y and x != y)

print(x == y or x != y)
print(x < y or x > y)
print(x <= y or x >= y)
print(x < y or x != y)

print(not x == y)
print(not x != y)
print(not x < y)
print(not x > y)
print(not x <= y)
print(not x >= y)