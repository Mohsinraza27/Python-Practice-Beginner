## A setis a collection which is unordered and unindexed,
## taht is iterable, mutable and has no duplicates elemets.
## In Python sets are written by curly brackets{ }


## Create_a_Set
s = {3, 7, 9, 12, 15 ,19, 22, 34}
print(s)
print(type(s))

s_1 = {}
print(s)
print(type(s_1))

s_2 = {2}
print(s_2)
print(type(s_2))


## Iterating_Through_a_Set
for a in s:
    print(a)
    