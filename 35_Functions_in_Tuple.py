## min(), max(), count(), index(), and sum()

t = (5, 10, 15, 20, 25, 30, 20, 45, 60, 20)
print(t)
print(type(t))

# min()
print(min(t))

# max()
print(max(t))

# count()
print(t.count(20))
print(t.count(15))

# index()
print(t.index(20))
print(t.index(10))
print(t.index(30))

# sum()
l = (23, 45, 67, 89, 90)
print(sum(t))
print(sum(l))
